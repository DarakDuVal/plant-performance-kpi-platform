#!/usr/bin/env node
// Generate VS Code custom chat modes from the agent role files.
//
// For every agent markdown file in the department folders, this writes a
// matching `.github/chatmodes/<name>.chatmode.md` file so each role can be
// selected from the Chat mode dropdown in VS Code (GitHub Copilot).
//
// The department `.md` files remain the single source of truth. Re-run this
// script whenever you add, remove, or edit an agent:
//
//   node scripts/generate-vscode-chatmodes.mjs
//
// Usage:
//   node scripts/generate-vscode-chatmodes.mjs          # write chat mode files
//   node scripts/generate-vscode-chatmodes.mjs --check   # fail if out of date

import { readFileSync, writeFileSync, mkdirSync, readdirSync, statSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const departments = [
  "data",
  "delivery-management",
  "design",
  "engineering",
  "marketing",
  "product",
  "security",
  "studio-operations",
  "testing",
];
const outDir = join(repoRoot, ".github", "chatmodes");

// Map the catalog tool names to the closest VS Code chat tools.
const TOOL_MAP = {
  Read: "codebase",
  Grep: "search",
  Glob: "search",
  Write: "editFiles",
  MultiEdit: "editFiles",
  Bash: "runCommands",
  WebSearch: "fetch",
};

function listAgentFiles() {
  const files = [];
  for (const dept of departments) {
    const dir = join(repoRoot, dept);
    let entries;
    try {
      entries = readdirSync(dir);
    } catch {
      continue;
    }
    for (const entry of entries) {
      const full = join(dir, entry);
      if (entry.endsWith(".md") && statSync(full).isFile()) {
        files.push(full);
      }
    }
  }
  return files.sort();
}

function parseFrontmatter(content) {
  if (!content.startsWith("---")) {
    throw new Error("missing frontmatter");
  }
  const end = content.indexOf("\n---", 3);
  if (end === -1) {
    throw new Error("unterminated frontmatter");
  }
  const block = content.slice(3, end).replace(/^\n/, "");
  const body = content.slice(content.indexOf("\n", end + 1) + 1).replace(/^\n+/, "");
  const fields = {};
  for (const line of block.split("\n")) {
    const match = /^([A-Za-z_]+)\s*:\s*(.*)$/.exec(line);
    if (match) {
      fields[match[1]] = match[2].trim();
    }
  }
  return { fields, body };
}

// Collapse the multi-line catalog description down to a single sentence that is
// suitable for the VS Code chat mode picker.
function shortDescription(description) {
  let text = description.replace(/\s*Examples?:\s*$/i, "").trim();
  const sentenceEnd = text.indexOf(". ");
  if (sentenceEnd !== -1) {
    text = text.slice(0, sentenceEnd + 1);
  }
  return text.replace(/\s+/g, " ").trim();
}

function mapTools(toolsField) {
  const mapped = [];
  for (const raw of toolsField.split(",")) {
    const name = raw.trim();
    const vscodeTool = TOOL_MAP[name];
    if (vscodeTool && !mapped.includes(vscodeTool)) {
      mapped.push(vscodeTool);
    }
  }
  mapped.sort();
  return mapped;
}

// Agent role files are identified by frontmatter that carries the required
// fields. Other markdown in the department folders (templates, diagrams) is
// skipped so it does not produce a chat mode.
function isAgentFile(content) {
  if (!content.startsWith("---")) {
    return false;
  }
  let fields;
  try {
    ({ fields } = parseFrontmatter(content));
  } catch {
    return false;
  }
  return Boolean(fields.name && fields.color && fields.tools);
}

function buildChatMode(sourcePath) {
  const content = readFileSync(sourcePath, "utf8");
  const { fields, body } = parseFrontmatter(content);
  const sourceRel = relative(repoRoot, sourcePath);
  const description = shortDescription(fields.description || fields.name || "");
  const tools = mapTools(fields.tools || "");
  const lines = [
    "---",
    `description: ${JSON.stringify(description)}`,
    `tools: [${tools.map((t) => `'${t}'`).join(", ")}]`,
    "---",
    "",
    `<!-- Generated from ${sourceRel} by scripts/generate-vscode-chatmodes.mjs.`,
    "     Edit the source agent file, then re-run the generator. Do not edit this file by hand. -->",
    "",
    body.trimEnd(),
    "",
  ];
  return { name: fields.name, content: lines.join("\n") };
}

function main() {
  const check = process.argv.includes("--check");
  mkdirSync(outDir, { recursive: true });
  let drift = 0;
  for (const sourcePath of listAgentFiles()) {
    if (!isAgentFile(readFileSync(sourcePath, "utf8"))) {
      continue;
    }
    const { name, content } = buildChatMode(sourcePath);
    const target = join(outDir, `${name}.chatmode.md`);
    let existing = null;
    try {
      existing = readFileSync(target, "utf8");
    } catch {
      // not generated yet
    }
    if (existing === content) {
      continue;
    }
    if (check) {
      console.error(`Out of date: ${relative(repoRoot, target)}`);
      drift += 1;
    } else {
      writeFileSync(target, content);
      console.log(`Wrote ${relative(repoRoot, target)}`);
    }
  }
  if (check && drift > 0) {
    console.error(`\n${drift} chat mode file(s) out of date. Run: node scripts/generate-vscode-chatmodes.mjs`);
    process.exit(1);
  }
  if (check) {
    console.log("All chat mode files are up to date.");
  }
}

main();
